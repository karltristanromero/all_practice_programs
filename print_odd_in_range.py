# Prog08: Create a program that print all the odd numbers starting from 0 to 100. (Use while loop)
'''
    set num

    while num <= 100
        if num is odd
            print(num)
        add + 1 to range_counter
'''

range_counter = 0

while range_counter <= 100:
    if range_counter % 2 != 0:
        print(range_counter)
